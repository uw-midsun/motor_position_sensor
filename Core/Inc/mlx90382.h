bool mlx90382_is_busy(void);

bool mlx90382_take_reading(uint16_t *reading);

void mlx90382_handle_spi_txrx_complete(SPI_HandleTypeDef *hspi);

void mlx90382_handle_spi_error(SPI_HandleTypeDef *hspi);

